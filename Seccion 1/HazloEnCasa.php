						<h3>Tomates cherry</h3>
						<p>Los tomates cherry son pequeños y fáciles de cultivar en una maceta mediana. Sigue estos pasos para plantarlos:</p>
						<ol>
							<li>Llena la maceta con tierra para macetas.</li>
							<li>Planta las semillas de tomate cherry a una profundidad de alrededor de 1/4 de pulgada.</li>
							<li>Riega la maceta regularmente, asegurándote de mantener la tierra húmeda pero no empapada.</li>
							<li>Ubica la maceta en un lugar soleado, como en un balcón o una ventana con buena exposición al sol.</li>
							<li>Cuando los tomates comiencen a crecer, asegúrate de atarlos a una estaca o un soporte para mantenerlos erguidos.</li>
						</ol>

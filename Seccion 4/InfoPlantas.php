					<h4>Insecticida orgánico:</h4>
					<p>El insecticida orgánico es una forma segura y natural de controlar plagas en las plantas. Se compone de ingredientes activos naturales como aceites esenciales y extractos de plantas. Se debe aplicar al follaje de la planta cuando se detectan plagas y repetir el proceso cada dos semanas hasta que la infestación haya desaparecido. Los beneficios incluyen el control de plagas sin dañar la planta ni los insectos beneficiosos. Es importante seguir las instrucciones del producto y utilizar guantes de protección al aplicar.</p>
					<img width="240px" height="150px" src="imagenes/insecticida_organico.jpg"/>
				</td>
			</tr>
			
			<tr>
				<td>
					<h4>Fungicida:</h4>
					<p>El fungicida es un producto utilizado para prevenir y tratar enfermedades fúngicas en las plantas. Se debe aplicar al follaje de la planta cada dos semanas durante la temporada de crecimiento para prevenir enfermedades, o al primer signo de enfermedad para tratarla. Los beneficios incluyen la prevención de enfermedades y la promoción de un crecimiento saludable de la planta. Es importante seguir las instrucciones del producto y utilizar guantes de protección al aplicar.</p>
					<img width="240px" height="150px" src="imagenes/fungicida.jpg"/>
				</td>
			</tr>
			
			<tr>
				<td>
					<h4>Agua destilada:</h4>
					<p>El agua destilada es agua pura que ha sido procesada para eliminar minerales y otros contaminantes. Se debe utilizar para regar las plantas en interiores y exteriores, ya que el agua del grifo puede contener cloro y otros productos químicos que pueden ser dañinos para las plantas. Los beneficios incluyen un suministro de agua pura para la planta y la prevención de la acumulación de sales y minerales en el suelo. Es importante no regar en exceso las plantas, ya que el exceso de agua puede ahogar las raíces.</p>
					<img width="240px" height="150px" src="imagenes/agua_destilada.jpg"/>
				</td>
			</tr>
			
			<tr>
				<td>
					<h4>Control de maleza:</h4>
					<p>El control de maleza es un producto utilizado para prevenir y tratar la aparición de maleza en el jardín. Se debe aplicar según las instrucciones del producto para evitar que la maleza crezca y compita con las plantas. Los beneficios incluyen un jardín limpio y saludable sin la competencia de la maleza. Es importante utilizar guantes de protección al aplicar y no utilizar en áreas donde se estén cultivando plantas comestibles.</p>

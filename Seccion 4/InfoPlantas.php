				<td>
					<h4>Nitrógeno:</h4>
					<p>El nitrógeno es uno de los nutrientes esenciales más importantes para el crecimiento de las plantas, ya que ayuda a formar proteínas y clorofila. Se debe aplicar al suelo en forma de fertilizante nitrogenado, ya sea orgánico o sintético. Los beneficios incluyen un crecimiento saludable de la planta, un follaje verde y una producción de flores y frutos más abundante. Es importante no aplicar en exceso el nitrógeno, ya que puede quemar las raíces de la planta y promover el crecimiento de maleza.</p>
					<img width="240px" height="150px" src="imagenes/nitrogeno.jpg"/>
				</td>
			</tr>

			<tr>
				<td>
					<h4>Fósforo:</h4>
					<p>El fósforo es un nutriente esencial para la formación de raíces y flores en las plantas. Se debe aplicar al suelo en forma de fertilizante fosfatado. Los beneficios incluyen un crecimiento saludable de las raíces, una floración abundante y una mejor calidad de los frutos. Es importante no aplicar en exceso el fósforo, ya que puede causar daño a las raíces y aumentar la acumulación de sales en el suelo.</p>
					<img width="240px" height="150px" src="imagenes/fosforo.jpg"/>
				</td>
			</tr>

			<tr>
				<td>
					<h4>Potasio:</h4>
					<p>El potasio es un nutriente esencial para la formación de frutos y semillas en las plantas, y también ayuda a mejorar la resistencia a enfermedades y al estrés. Se debe aplicar al suelo en forma de fertilizante potásico. Los beneficios incluyen una producción de frutos y semillas más abundante y una mayor resistencia a enfermedades y al estrés ambiental. Es importante no aplicar en exceso el potasio, ya que puede disminuir la absorción de otros nutrientes y aumentar la salinidad del suelo.</p>
					<img width="240px" height="150px" src="imagenes/potasio.jpg"/>
				</td>
			</tr>

			<tr>
				<td>
					<h4>Calcio:</h4>
					<p>El calcio es un nutriente esencial para el crecimiento de las paredes celulares en las plantas y ayuda a prevenir la pudrición apical en los tomates y otros frutos. Se debe aplicar al suelo en forma de cal agrícola o harina de hueso. Los beneficios incluyen un crecimiento saludable de las paredes celulares y una prevención de la pudrición apical en los tomates y otros frutos. Es importante no aplicar en exceso el calcio, ya que puede disminuir la absorción de otros nutrientes y aumentar la alcalinidad del suelo.</p>
					<img width="240px" height="150px" src="imagenes/calcio.webp"/>
				</td>
			</tr>

